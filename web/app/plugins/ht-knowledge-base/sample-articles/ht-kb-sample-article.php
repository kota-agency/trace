<h3>The Right Tuxedo for Any Content</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Your content deserves the best. The site should conform to your content, not the other way around. WordPress comes with a full theme system which makes designing everything from the simplest site to the most complicated portal a piece of cake. Have a new design every day. Your ideas should look as good as what they say.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Everything you see here, from the documentation to the code itself, was created by and for the community. WordPress is an Open Source project, which means there are hundreds of people all over the world working on it. (More than most commercial platforms.) It also means you are free to use it for anything from your cat’s home page to a Fortune 500 web site without paying anyone a license fee and a number of other important freedoms.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3>Obsessively Organized</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>It doesn’t matter how much content you have, if your visitors can’t find it. WordPress organizes your content by day, by month, by year, by author, by category — any way you can describe it — and dynamically creates browsable archives so things always stay up to date.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>WordPress has killer search baked in. Every word you write is fully searchable through a single box at the top of each page and if your users choose to use an external search engine like Google, rest assured, WordPress will present your content in a way that all but ensures it makes it to the top of the results every time</p>
