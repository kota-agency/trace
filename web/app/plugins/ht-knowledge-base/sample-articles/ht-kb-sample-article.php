<p>WordPress started in 2003 with a single bit of code to enhance the typography of everyday writing and with fewer users than you can count on your fingers and toes. Since then it has grown to be the largest self-hosted blogging tool in the world, used on millions of sites and seen by tens of millions of people every day.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The following is a list of some of the features that come standard with WordPress, however there are literally tens of thousands of plugins that extend what WordPress does, so the actual functionality is nearly limitless. You are also free to do whatever you like with the WordPress code, extend it or modify in any way or use it for commercial projects without any licensing fees. That is the beauty of <a href="#">free software</a>, free meaning not only price but also the freedom to have complete control over it.</p>
<!-- /wp:paragraph -->

